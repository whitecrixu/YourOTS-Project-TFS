-- begin cyclopedia cache
CYCLOPEDIA_CACHE = {
	outfitLookTypes = {
		[PLAYERSEX_FEMALE] = {},
		[PLAYERSEX_MALE] = {}
	},
	mounts = {},
	familiars = {},
}

-- init cyclopedia cache
for playerSex = PLAYERSEX_FEMALE, PLAYERSEX_MALE do
	for _, outfit in pairs(Game.getOutfits(playerSex)) do
		CYCLOPEDIA_CACHE.outfitLookTypes[playerSex][#CYCLOPEDIA_CACHE.outfitLookTypes[playerSex] + 1] = outfit.lookType
	end
end

for _, mount in pairs(Game.getMounts()) do
	CYCLOPEDIA_CACHE.mounts[#CYCLOPEDIA_CACHE.mounts + 1] = mount
end

for _, familiar in pairs(Game.getFamiliars()) do
	CYCLOPEDIA_CACHE.familiars[#CYCLOPEDIA_CACHE.familiars + 1] = familiar
end
-- end cyclopedia cache